public class Calculadora {
    
    public static void subtracao (float numeroUm, float numeroDois) {
        float resultado = numeroUm - numeroDois;
        System.out.println("O Resultado da adição é: " + resultado);
    }

    public static void adicao (float numeroUm, float numeroDois) {
        float resultado = numeroUm + numeroDois;
        System.out.println("O Resultado da substração é: " + resultado);    }

    public static void multiplicacao (float numeroUm, float numeroDois) {
        float resultado = numeroUm * numeroDois;
        System.out.println("O Resultado da multiplicação é: " + resultado);    }

    public static void divisao (float numeroUm, float numeroDois) {
        float resultado = numeroUm / numeroDois;
        System.out.println("O Resultado da divisão: " + resultado);    }

}